package com.app.superficcion;

//Opciones para filtro
public class Opciones {
    int id;
    String name;

    public Opciones(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
